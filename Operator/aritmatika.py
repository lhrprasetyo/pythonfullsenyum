# python operator aritmatika

x = 10
y = 5

# 1.penjumlahan(+)
print(f"{x} + {y} = {x + y}\n")

# 2.pengurangan(-) 
print(f"{x} - {y} = {x - y}\n")

# 3.perkalian(*)
print(f"{x} * {y} = {x * y}\n")

# 4.pembagian(/)
print(f"{x} / {y} = {x / y }\n")

# 5.eksponensial(**)
print(f"{x} ** {y} = {x ** y}\n")

# 6.Modulus / sisa bagi(%) merupakan sisa bagi, misal 100/3 kan 33.3 , nah kalo ini, hasilnya 33, trus modulus nya 1
print(f"{x} % {y} = {x % y }\n")

# 7.division flor (//)fungsinya mirip pembagian,tp hasilnya merupakan bilangan bulat,
# dan memiliki sisa contohmisal 100/3 kan 33.3 , nah kalo ini, hasilnya 33
print(f"{x} // {y} = {x // y}\n")