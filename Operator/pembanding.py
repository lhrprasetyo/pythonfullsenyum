x = 10
y = 5

# 1.Equal / sama dengan == 
print(f"{x} == {y} = {x == y}\n")

# 2.not equal / tidak sama dengan (!=)
print(f"{x} != {y} = {x != y}\n")

# 3.Greater than / Lebih besar dari (>)
print(f"{x} > {y} = {x > y}\n")

# 4.Less than / lebih kecil dari (<)
print(f"{x} < {y} = {x < y}\n")

# 5.Greater than or Equal to / lebih besar atau sama dengan dari (>=)
print(f"{x} >= {y} = {x >= y}\n")

# 6.Less than or Equal to / lebih kecil atau sama dengan dari (<=)?
print(f"{x} <= {y} = {x <= y}\n")